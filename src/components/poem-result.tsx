
'use client';

import Image from 'next/image';
import { Card, CardContent, CardHeader, CardTitle, CardFooter } from '@/components/ui/card';
import { Skeleton } from '@/components/ui/skeleton';
import { Button } from '@/components/ui/button';
import { Share2, BookOpen, Music4, LoaderCircle, Save, Download } from 'lucide-react';
import { useToast } from '@/hooks/use-toast';
import AudioPlayer from '@/components/audio-player';
import { Carousel, CarouselContent, CarouselItem, CarouselNext, CarouselPrevious, type CarouselApi } from '@/components/ui/carousel';
import { useEffect, useState, useRef } from 'react';
import type { User } from 'firebase/auth';
import html2canvas from 'html2canvas';
import jsPDF from 'jspdf';
import { DropdownMenu, DropdownMenuContent, DropdownMenuItem, DropdownMenuTrigger } from '@/components/ui/dropdown-menu';

type PoemResultProps = {
  poems: string[] | null;
  selectedPoem: string | null;
  onPoemSelect: (poem: string) => void;
  imagePreview: string | null;
  isLoading: boolean;
  audioDataUri: string | null;
  isNarrating: boolean;
  onNarrate: () => void;
  onSave: () => void;
  isSaving: boolean;
  user: User | null;
  detectedTone: string | null;
};

export default function PoemResult({ poems, selectedPoem, onPoemSelect, imagePreview, isLoading, audioDataUri, isNarrating, onNarrate, onSave, isSaving, user, detectedTone }: PoemResultProps) {
  const { toast } = useToast();
  const [carouselApi, setCarouselApi] = useState<CarouselApi>()
  const [current, setCurrent] = useState(0)
  const posterRef = useRef<HTMLDivElement>(null);

  useEffect(() => {
    if (!carouselApi) {
      return
    }
 
    setCurrent(carouselApi.selectedScrollSnap() + 1)
 
    carouselApi.on("select", () => {
      const selectedIndex = carouselApi.selectedScrollSnap();
      setCurrent(selectedIndex + 1)
      if(poems) {
        onPoemSelect(poems[selectedIndex]);
      }
    })
  }, [carouselApi, poems, onPoemSelect])

  const handleDownload = async (format: 'png' | 'jpeg' | 'pdf') => {
    if (!posterRef.current) {
        toast({ variant: "destructive", title: "Download Failed", description: "Could not find the poster element." });
        return;
    }

    try {
        const canvas = await html2canvas(posterRef.current, { 
            useCORS: true,
            allowTaint: true,
            backgroundColor: window.getComputedStyle(document.body).getPropertyValue('background-color'),
            onclone: (document) => {
                // On the cloned document, find the poster element and make it visible for capture
                const posterElement = document.getElementById(posterRef.current!.id);
                if (posterElement) {
                    posterElement.style.color = window.getComputedStyle(document.documentElement).getPropertyValue('--foreground');
                }
            }
        });

        if (format === 'pdf') {
            const imgData = canvas.toDataURL('image/png');
            const pdf = new jsPDF({
                orientation: 'portrait',
                unit: 'px',
                format: [canvas.width, canvas.height]
            });
            pdf.addImage(imgData, 'PNG', 0, 0, canvas.width, canvas.height);
            pdf.save('photo-poem.pdf');
        } else {
            const image = canvas.toDataURL(`image/${format}`, 1.0);
            const link = document.createElement('a');
            link.href = image;
            link.download = `photo-poem.${format}`;
            document.body.appendChild(link);
            link.click();
            document.body.removeChild(link);
        }
        toast({ title: "Download Started", description: `Your poster is being downloaded as a ${format.toUpperCase()} file.` });

    } catch (error) {
        console.error("Download error: ", error);
        toast({ variant: "destructive", title: "Download Failed", description: "An error occurred while generating the poster." });
    }
  };


  const shareOnTwitter = () => {
    if (!selectedPoem) return;
    const text = `A poem inspired by a photo:\n\n${selectedPoem}\n\nGenerated by #PhotoPoet`;
    const url = `https://twitter.com/intent/tweet?text=${encodeURIComponent(text)}`;
    window.open(url, '_blank');
  };

  const shareOnFacebook = () => {
    if (!selectedPoem) return;
    const quote = `A poem inspired by a photo:\n\n${selectedPoem}\n\nGenerated by #PhotoPoet`;
    const url = `https://www.facebook.com/sharer/sharer.php?u=${encodeURIComponent(window.location.href)}&quote=${encodeURIComponent(quote)}`;
    window.open(url, '_blank');
  };

  const shareOnInstagram = () => {
    if (navigator.clipboard && selectedPoem) {
      navigator.clipboard.writeText(`A poem inspired by a photo:\n\n${selectedPoem}\n\nGenerated by #PhotoPoet`);
      toast({
        title: "Poem Copied!",
        description: "The poem has been copied to your clipboard. Open Instagram to share it!",
      });
    } else {
      toast({
        variant: "destructive",
        title: "Copy Failed",
        description: "Could not copy poem to clipboard.",
      });
    }
  };
  
  return (
    <Card className="shadow-md animate-in fade-in-50 slide-in-from-bottom-10 duration-500">
      <CardHeader>
        <CardTitle className="flex items-center justify-between gap-2 font-headline">
          <div className="flex items-center gap-2">
            <BookOpen className="text-accent" />
            Poetic Vision
          </div>
          <div className="flex items-center gap-2">
            {selectedPoem && !isLoading && (
              <DropdownMenu>
                <DropdownMenuTrigger asChild>
                  <Button variant="outline" size="sm">
                    <Download className="mr-2 h-4 w-4" />
                    Download
                  </Button>
                </DropdownMenuTrigger>
                <DropdownMenuContent>
                  <DropdownMenuItem onClick={() => handleDownload('png')}>PNG</DropdownMenuItem>
                  <DropdownMenuItem onClick={() => handleDownload('jpeg')}>JPG</DropdownMenuItem>
                  <DropdownMenuItem onClick={() => handleDownload('pdf')}>PDF</DropdownMenuItem>
                </DropdownMenuContent>
              </DropdownMenu>
            )}
            {selectedPoem && !isLoading && user && (
              <Button onClick={onSave} disabled={isSaving} variant="outline" size="sm">
                {isSaving ? (
                  <>
                    <LoaderCircle className="mr-2 h-4 w-4 animate-spin" />
                    Saving...
                  </>
                ) : (
                  <>
                    <Save className="mr-2 h-4 w-4" />
                    Save to Journal
                  </>
                )}
              </Button>
            )}
            {selectedPoem && !isLoading && (
              <Button onClick={onNarrate} disabled={isNarrating} variant="outline" size="sm">
                {isNarrating ? (
                  <>
                    <LoaderCircle className="mr-2 h-4 w-4 animate-spin" />
                    Generating...
                  </>
                ) : (
                  <>
                    <Music4 className="mr-2 h-4 w-4" />
                    Listen to Poem
                  </>
                )}
              </Button>
            )}
          </div>
        </CardTitle>
      </CardHeader>
      <CardContent>
        <div id="poster-container" ref={posterRef} className="grid grid-cols-1 md:grid-cols-2 gap-6 items-start">
            <div className="relative w-full aspect-square rounded-lg overflow-hidden shadow-lg">
            {isLoading && !imagePreview ? (
                <Skeleton className="h-full w-full" />
            ) : (
                imagePreview && (
                <Image
                    src={imagePreview}
                    alt="Source for poem"
                    layout="fill"
                    objectFit="cover"
                    data-ai-hint="user image"
                    crossOrigin="anonymous"
                />
                )
            )}
            </div>
            <div className="space-y-4">
            {detectedTone && !isLoading && (
                <div className="text-sm text-center text-muted-foreground italic">
                Detected Tone: {detectedTone}
                </div>
            )}
            {isLoading ? (
                <div className="space-y-3">
                <Skeleton className="h-4 w-4/5" />
                <Skeleton className="h-4 w-full" />
                <Skeleton className="h-4 w-3/4" />
                <Skeleton className="h-4 w-full" />
                <Skeleton className="h-4 w-2/3" />
                </div>
            ) : (
                poems && (
                <Carousel setApi={setCarouselApi} className="w-full">
                    <CarouselContent>
                    {poems.map((poem, index) => (
                        <CarouselItem key={index}>
                        <p className="text-foreground/90 whitespace-pre-wrap leading-relaxed italic p-1">
                            {poem}
                        </p>
                        </CarouselItem>
                    ))}
                    </CarouselContent>
                    { poems.length > 1 &&
                    <>
                        <CarouselPrevious />
                        <CarouselNext />
                        <div className="py-2 text-center text-sm text-muted-foreground">
                        Poem {current} of {poems.length}
                        </div>
                    </>
                    }
                </Carousel>
                )
            )}
            </div>
        </div>
      </CardContent>
      {audioDataUri && <AudioPlayer audioDataUri={audioDataUri} />}
      {selectedPoem && !isLoading && (
        <CardFooter className="flex justify-end gap-2 flex-wrap">
            <Button variant="outline" onClick={shareOnTwitter}>
                <Share2 className="mr-2 h-4 w-4" /> Share on X
            </Button>
            <Button variant="outline" onClick={shareOnFacebook}>
                <Share2 className="mr-2 h-4 w-4" /> Share on Facebook
            </Button>
            <Button variant="outline" onClick={shareOnInstagram}>
                <Share2 className="mr-2 h-4 w-4" /> Share on Instagram
            </Button>
        </CardFooter>
      )}
    </Card>
  );
}
